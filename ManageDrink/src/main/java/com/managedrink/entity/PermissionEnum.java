package com.managedrink.entity;

/**
 * Enum đại diện cho các quyền hạn trong hệ thống.
 * Enum này xác định các quyền hạn cơ bản mà người dùng hoặc thực thể có thể có.
 */
public enum PermissionEnum {

    /**
     * Quyền đọc dữ liệu.
     */
    READ,

    /**
     * Quyền ghi dữ liệu.
     */
    WRITE,

    /**
     * Quyền xóa dữ liệu.
     */
    DELETE

}
